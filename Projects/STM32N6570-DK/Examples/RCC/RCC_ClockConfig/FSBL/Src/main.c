/**
  * @brief  switch in system clock out of ISR context.
  * @retval None
  */
static void SwitchSystemClock(void)
{
  if (__HAL_RCC_GET_SYSCLK_SOURCE() == RCC_SYSCLKSOURCE_STATUS_IC2_IC6_IC11)
  {
    /* Set SYSB frequency to 48000000 Hz clocked by HSE  */
    SystemClockHSE_Config();
  }
  else if (__HAL_RCC_GET_SYSCLK_SOURCE() == RCC_SYSCLKSOURCE_STATUS_HSE)
  {
    /* Set SYSB frequency to 64000000 Hz clocked by HSI  */
    SystemClockHSI_Config();
  }
  else if (__HAL_RCC_GET_SYSCLK_SOURCE() == RCC_SYSCLKSOURCE_STATUS_HSI)
  {
    /* Set SYSB frequency to 400000000 Hz clocked by IC2 output  */
    SystemClockIC2_IC6_IC11_Config();
  }
  /* reset global variable */
  SwitchClock = RESET;
}

